mangga = 10
apel = 5
manggis = 20
anggur = 15

# penjumlahan
hasil = mangga + apel
print("hasil {mangga} + {apel} = {hasil} ".format(mangga=10,apel=5,hasil=hasil))

# pengurangan
hasil = manggis - mangga
print("hasil {manggis} - {mangga} = {hasil} ".format(manggis=20,mangga=10,hasil=hasil))

# perkalian
hasil = anggur * manggis
print("hasil {anggur} * {manggis} = {hasil} ".format(anggur=15,manggis=20,hasil=hasil))

# pembagian
hasil = manggis / apel
print("hasil {manggis} / {apel} = {hasil} ".format(manggis=20,apel=5,hasil=hasil))


